<template>
  <el-form :model="ruleForm2" :rules="rules2" ref="ruleForm2" label-position="left" label-width="0px" class="demo-ruleForm login-container">
    <h3 class="title">系统登录</h3>
    <el-form-item prop="mobile">
      <el-input type="text" v-model="ruleForm2.mobile" auto-complete="off" placeholder="账号"></el-input>
    </el-form-item>
    <el-form-item prop="password">
      <el-input type="password" v-model="ruleForm2.password" auto-complete="off" placeholder="密码"></el-input>
    </el-form-item>
    <el-checkbox v-model="checked" checked class="remember">记住密码</el-checkbox>
    <el-form-item style="width:100%;">
      <el-button type="primary" style="width:100%;" @click="handleSubmit2" :loading="logining">登录</el-button>
      <!--<el-button @click.native.prevent="handleReset2">重置</el-button>-->
    </el-form-item>
  </el-form>
</template>

<script>
  export default {
    data() {
      return {
        logining: false,
        ruleForm2: {
          mobile: '',
          password: ''
        },
        rules2: {
          mobile: [
            { required: true, message: '请输入账号', trigger: 'blur' },
            //{ validator: validaePass }
          ],
          password: [
            { required: true, message: '请输入密码', trigger: 'blur' },
            //{ validator: validaePass2 }
          ]
        },
        checked: true
      };
    },
    methods: {
      handleReset2() {
        this.$refs.ruleForm2.resetFields();
      },
      handleSubmit2() {
        const self = this;
        this.logining = true;
        var loginParams = { 
            "HEAD":{},
            "BODY":{
                mobile: this.ruleForm2.mobile, 
                password: this.ruleForm2.password 
            }
        };
        this.$http.post(this.baseCont.JAVA_API + 'SYS_001',loginParams).then(data => {
          this.logining = false;
          let { MSG, CODE } = data.body.HEAD;
          if (CODE !== '000') {
            this.$message({
              message: MSG,
              type: 'error'
            });
          } else {
            self.$router.push({ path: '/' });
            let token = data.body.BODY.TOKEN;
            localStorage.setItem('token',token);
          }
        });
      }
    },
    created() {
      window.addEventListener('token', ()=> {
        this.token = localStorage.getItem('watchStorage');
      })
    }
  }

</script>

